export function getFurnitureTemplate(templateId: string): FurnitureTemplate | undefined {
  return FURNITURE_TEMPLATES.find((t) => t.id === templateId);
}

export function getFurnitureByCategory(category: FurnitureCategory): FurnitureTemplate[] {
  return FURNITURE_TEMPLATES.filter((t) => t.category === category);
}

export function getCategoryInfo(categoryId: FurnitureCategory) {
  return FURNITURE_CATEGORIES.find((c) => c.id === categoryId);
}

export function getAllCategories(): FurnitureCategory[] {
  return FURNITURE_CATEGORIES.map((c) => c.id);
}
